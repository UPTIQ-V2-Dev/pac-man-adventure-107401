export const countPellets = (maze: CellType[][]): number => {
  let count = 0;
  for (let row = 0; row < maze.length; row++) {
    for (let col = 0; col < maze[row].length; col++) {
      if (maze[row][col] === PELLET || maze[row][col] === POWER_PELLET) {
        count++;
      }
    }
  }
  return count;
};

export const createInitialMaze = (): CellType[][] => {
  return MAZE_LAYOUT.map(row => [...row]);
};